local map = vim.api.nvim_set_keymap
local vim = vim.cmd
local default_opts = {noremap = true, silent = true}

------------------------------------------------------
----------------------NEOTREE-------------------------
vim('nnoremap <C-n> :NeoTreeFloatToggle<CR>')
------------------------------------------------------
------------------------------------------------------

------------------------------------------------------
----------------------TELESCOPE-----------------------
vim('nnoremap <A-t> :Telescope<CR>')
------------------------------------------------------
------------------------------------------------------

------------------------------------------------------
----------------------SIDEBAR-------------------------
vim('nnoremap <C-s> :SidebarNvimToggle<CR>')
------------------------------------------------------
------------------------------------------------------

------------------------------------------------------
----------------------BUFFERLINE----------------------
vim('nnoremap <A-l> :BufferLineCycleNext<CR>')
vim('nnoremap <A-h> :BufferLineCyclePrev<CR>')
vim('nnoremap <A-d> :BufferLineCloseRight<CR>')
vim('nnoremap <A-s> :BufferLineCloseLeft<CR>')

------------------------------------------------------
--------------------Move Window-----------------------
vim('nnoremap <C-w> :w<CR>')
vim('nnoremap <A-q> :q<CR>')
map('n', '<C-h>', '<C-w>h', default_opts)
map('n', '<C-l>', '<C-w>l', default_opts)
map('n', '<C-j>', '<C-w>j', default_opts)
map('n', '<C-k>', '<C-w>k', default_opts)
------------------------------------------------------
